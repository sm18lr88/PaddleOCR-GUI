def detect_ui_language() -> UiLanguage:
    language_code = (locale.getlocale()[0] or "en").split("_")[0].lower()
    language_map = {
        "ar": UiLanguage.ARABIC,
        "es": UiLanguage.SPANISH,
        "he": UiLanguage.HEBREW,
        "iw": UiLanguage.HEBREW,
        "ja": UiLanguage.JAPANESE,
        "ko": UiLanguage.KOREAN,
        "zh": UiLanguage.CHINESE,
    }
    return language_map.get(language_code, UiLanguage.ENGLISH)


def text(language: UiLanguage, key: TextKey) -> str:
    return TEXT[language][key]
